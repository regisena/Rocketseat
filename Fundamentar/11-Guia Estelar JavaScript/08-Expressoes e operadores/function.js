// Operadores de atribuição (Assignment)
let x;
console.log(x); // undefined

// assignment
x = 1;
console.log(x); // 1

// addition assignment
x += 2;
console.log(x); // 3

// subtraction assignment
x -= 1;
console.log(x); // 2

// multiplication assignment
x *= 2;
console.log(x); // 4

// division assignment
x /= 2;
console.log(x); // 2
